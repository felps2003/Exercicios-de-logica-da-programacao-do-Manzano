let comprimento, altura, largura, volume;

altura = parseFloat(prompt("Qual é a altura da caixa? "))

largura = parseFloat(prompt("Qual é a largura da caixa? "))

comprimento = parseFloat(prompt("Qual é o comprimento da caixa? "))

volume = comprimento*largura*altura;

alert("O volume da Caixa é de " + volume + "m³")